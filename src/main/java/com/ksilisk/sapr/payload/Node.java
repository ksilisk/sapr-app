package com.ksilisk.sapr.payload;

public class Node {
    double XLoad;
    double YLoad;

    public Node() {
    }

    public double getXLoad() {
        return XLoad;
    }

    public void setXLoad(double XLoad) {
        this.XLoad = XLoad;
    }

    public double getYLoad() {
        return YLoad;
    }

    public void setYLoad(double YLoad) {
        this.YLoad = YLoad;
    }
}
